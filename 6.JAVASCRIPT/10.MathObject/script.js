/* Javascript Math Object Methods */

// The Math namespace object contains static properties and methods for mathematical constants and functions.


// Math.PI

{
    let math1 = Math.PI
    console.log(math1);
    console.log(22/7);
    
}

// Math.abs()

{
    let math2 = Math.abs(10)
    let math3 = Math.abs(-10)

    console.log(math2);
    console.log(math3);

}

// Math.floor()

{
    let math4 = Math.floor(52.6)

    console.log(math4);
    
}

// Math.ceil()

{
    let math = Math.ceil(52.1)

    console.log(math);
}

// Math.trunc()

{
    let math = Math.trunc(-10.9)

    console.log(math);
}

// Math.sqrt()

{
    let math = Math.sqrt(64)
    let math1 = Math.sqrt(121)

    console.log(math);
    console.log(math1);
}

// Math.random()

{
    let math = Math.trunc(Math.random()*10000)

    console.log(math);
}

// Math.sin()

{
    let math = Math.sin(30*3.14/180)

    console.log(math);
}

// Math.cos()

{
    let math = Math.cos(90*3.14/180)

    console.log(math);
}

// Math.sign()

{
    let math = Math.sign(-50)

    console.log(math);
}

// Math.pow()

{
    let math = Math.pow(5 , 5)

    console.log(math);
}

// Math.round()

{
    let math = Math.round(-25.40)

    console.log(math);
}

// Math.max
// Math.min

{
    let math = Math.max(10 , 20 , 85 , 101 , 9)
    let math1 = Math.min(10 , 20 , 85 , 101 , 9)

    console.log(math);
    console.log(math1);
}


// Math.log

{
    let math = Math.log(0)
    let math1 = Math.log10(15.27)
    let math2 = Math.log2(15.27)
    let math3 = Math.log(1)

    console.log(math);
    console.log(math1);
    console.log(math2);
    console.log(math3);
}

